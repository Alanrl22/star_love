package com.starlove.starlove.entity;

public class Planet {

    private int id;
    private String name;


    public Planet() {

    }

    public Planet(int id, String name) {

        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}